/// <summary>
/// 시작/종료가 가능한 공격 행동의 공용 계약.
/// BeginAttack 으로 공격 시퀀스(차징→발사, 조준→투사, 폭발 등)를 개시하고,
/// EndAttack 으로 즉시 취소/종료한다.
///
/// 컨트롤러(쿨다운/감지 등 타이밍을 소유하는 쪽)는 이 인터페이스만 알면 되므로
/// 레이저, 미사일, 폭탄 어떤 공격 구현이든 동일 컨트롤러에 플러그인된다.
///
/// 멱등성 약속: 이미 수행 중인 상태에서 같은 메서드가 다시 호출되어도
/// 구현은 크래시 없이 안전하게 처리해야 한다.
/// </summary>
public interface IAttackBehavior
{
    void BeginAttack();
    void EndAttack();
}
